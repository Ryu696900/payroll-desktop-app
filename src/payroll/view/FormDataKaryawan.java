    private void tampilComboJabatan() {
        try {
            String sql = "SELECT * FROM jabatan";
            java.sql.Connection conn = (java.sql.Connection) payroll.database.Koneksi.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            java.sql.ResultSet rs = pst.executeQuery();
            
            while(rs.next()){
                cmbJabatan.addItem(rs.getString("kode_jabatan") 
                        + " - " 
                        + rs.getString("nama_jabatan"));
            }
        
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    private void kosongkanForm() {
        txtNIK.setEditable(true); 
        txtNIK.setText(null);
        txtNama.setText(null);
        txtTgl.setText(null);
        txtAlamat.setText(null);
        txtTelp.setText(null);
        
        if(cmbJabatan.getItemCount() > 0) {
            cmbJabatan.setSelectedIndex(0);
        }
        
        cmbStatus.setSelectedIndex(0);
    }
    
    private void tampilData() {
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel();
        model.addColumn("NIK");
        model.addColumn("Nama");
        model.addColumn("Tgl Lahir");
        model.addColumn("Jabatan");
        model.addColumn("Alamat");
        model.addColumn("No Telp");
        model.addColumn("Status");
        
        try {
            String sql = "SELECT k.nik, k.nama_karyawan, k.tgl_lahir, j.nama_jabatan, k.alamat, k.no_telp, k.status_menikah " +
                         "FROM karyawan k JOIN jabatan j ON k.kode_jabatan = j.kode_jabatan";
            
            java.sql.Connection conn = (java.sql.Connection) payroll.database.Koneksi.configDB();
            java.sql.Statement st = conn.createStatement();
            java.sql.ResultSet rs = st.executeQuery(sql);
            
            while(rs.next()) {
                model.addRow(new Object[] {
                    rs.getString("nik"),
                    rs.getString("nama_karyawan"),
                    rs.getString("tgl_lahir"),
                    rs.getString("nama_jabatan"),
                    rs.getString("alamat"),
                    rs.getString("no_telp"),
                    rs.getString("status_menikah")
                });
            }
            
            tblKaryawan.setModel(model);
        
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
