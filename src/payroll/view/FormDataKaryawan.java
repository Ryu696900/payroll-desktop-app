    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            String selectedJabatan = cmbJabatan.getSelectedItem().toString();
            String[] parts = selectedJabatan.split(" - ");
            String kodeJabatanFix = parts[0];
            String sql = "INSERT INTO karyawan VALUES ('"
                    + txtNIK.getText() + "','"
                    + txtNama.getText() + "','"
                    + txtTgl.getText() + "','"    
                    + txtAlamat.getText() + "','"
                    + txtTelp.getText() + "','"
                    + cmbStatus.getSelectedItem() + "','"
                    + kodeJabatanFix + "')";
            
            java.sql.Connection conn = (java.sql.Connection) payroll.database.Koneksi.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
            
            javax.swing.JOptionPane.showMessageDialog(null, "Data Karyawan Berhasil Disimpan");
            tampilData(); 
        
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal Simpan: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        try {
            int jawab = javax.swing.JOptionPane.showConfirmDialog(this, "Yakin hapus karyawan ini?", "Konfirmasi", javax.swing.JOptionPane.YES_NO_OPTION);
            
            if (jawab == javax.swing.JOptionPane.YES_OPTION) {
                String sql = "DELETE FROM karyawan WHERE nik ='" + txtNIK.getText() + "'";
                java.sql.Connection conn = (java.sql.Connection) payroll.database.Koneksi.configDB();
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                pst.execute();
                
                javax.swing.JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus");
                tampilData();
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnHapusActionPerformed

