    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try {
            String selectedJabatan = cmbJabatan.getSelectedItem().toString();
            String[] parts = selectedJabatan.split(" - ");
            String kodeJabatanFix = parts[0];
            
            String sql = "UPDATE karyawan SET "
                    + "nama_karyawan='" + txtNama.getText() + "',"
                    + "tgl_lahir='" + txtTgl.getText() + "',"
                    + "alamat='" + txtAlamat.getText() + "',"
                    + "no_telp='" + txtTelp.getText() + "',"
                    + "status_menikah='" + cmbStatus.getSelectedItem() + "',"
                    + "kode_jabatan='" + kodeJabatanFix + "' "
                    + "WHERE nik='" + txtNIK.getText() + "'";
            
            java.sql.Connection conn = (java.sql.Connection) payroll.database.Koneksi.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
            
            javax.swing.JOptionPane.showMessageDialog(null, "Data Berhasil Diedit");
            tampilData();
            kosongkanForm();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal Edit: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed

