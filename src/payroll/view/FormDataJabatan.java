    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            String sql = "INSERT INTO jabatan VALUES ('"
                    + txtKode.getText() + "','"
                    + txtNamaJabatan.getText() + "','"
                    + txtGajiPokok.getText() + "','"
                    + txtTunjangan.getText() + "')";
            
            java.sql.Connection conn = (java.sql.Connection) Koneksi.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
            
            JOptionPane.showMessageDialog(null, "Penyimpanan Data Berhasil");
            
            tampilData();
            kosongkanForm();
        
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try {
            String sql = "UPDATE jabatan SET nama_jabatan = '" + txtNamaJabatan.getText()
                    + "', gaji_pokok = '" + txtGajiPokok.getText()
                    + "', tunjangan_transport = '" + txtTunjangan.getText()
                    + "' WHERE kode_jabatan = '" + txtKode.getText() + "'";
            
            java.sql.Connection conn = (java.sql.Connection) Koneksi.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
            
            JOptionPane.showMessageDialog(null, "Data Berhasil Diedit");
            tampilData();
            kosongkanForm();
        
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        try {
            int jawab = JOptionPane.showConfirmDialog(this, "Yakin hapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            
            if (jawab == JOptionPane.YES_OPTION) {
                String sql = "DELETE FROM jabatan WHERE kode_jabatan ='" + txtKode.getText() + "'";
                java.sql.Connection conn = (java.sql.Connection) Koneksi.configDB();
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                pst.execute();
                
                JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus");
                tampilData();
                kosongkanForm();
    }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnHapusActionPerformed

