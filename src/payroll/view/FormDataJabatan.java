private void tampilData() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("Kode");
    model.addColumn("Nama Jabatan");
    model.addColumn("Gaji Pokok");
    model.addColumn("Tunjangan");
    
    try {
        String sql = "SELECT * FROM jabatan";
        Connection conn = (Connection) Koneksi.configDB();
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(sql);
            while(rs.next()) {
                model.addRow(new Object[] {
                    rs.getString("kode_jabatan"),
                    rs.getString("nama_jabatan"),
                    rs.getString("gaji_pokok"),
                    rs.getString("tunjangan_transport")
                });
            }
            tblJabatan.setModel(model);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal Load Data: " + e.getMessage());
    }
}
